/**
 * AssignmentOperators
 */
public class AssignmentOperators {

  public static void main(String[] args) {
    int x = 5;
    x += 5; // x = x + 5;
    System.out.println(x); // 10
    x -= 3; // x = x - 3;
    System.out.println(x);  // 7
    x *= 2; // x = x * 2;
    System.out.println(x); // 14
    x /= 2; // x = x / 2;
    System.out.println(x);  // 7
    x %= 2; // x = x % 2;
    System.out.println(x);  // 1
  }
}