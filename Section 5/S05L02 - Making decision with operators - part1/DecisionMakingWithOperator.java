public class DecisionMakingWithOperator {

    public static void main(String[] args) {
      int x=5;
      if (x == 5) {
        System.out.println("Value of x is 5!");
      } 
      else {
        System.out.println("Value of x is NOT 5!");
      }

      // If there is only one statement after the if or else, you can leave the {} away but is not recommended
      // if (x == 5) System.out.println("Value of x is 5!");
      // else System.out.println("Value of x is NOT 5!");

    }
}